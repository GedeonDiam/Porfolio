        <h1 class="text-center"><u>Qu'est ce que le BTS SIO?</u></h1><br>

        <p>Le <strong>Brevet de Technicien Supérieur (BTS) Services informatique aux Organisations (SIO) </strong>
            s’adresse à ceux qui souhaitent se former en deux ans (reconnu par l’état de niveau <strong>Bac+2</strong>)
            aux métiers d’administrateur réseau ou de développeur pour intégrer directement le marché du travail ou
            continuer des études dans le domaine de l’informatique. Il associe aux enseignements théoriques et
            professionnels (en contrôle continu et final) deux stages en entreprise (10 à 11 semaines réparties sur les
            deux années d’études). <br><br>

            Ce BTS propose deux spécialités distinctes : <strong>SISR et SLAM</strong> .</p>

        <div class="container">
